//Operators
/*
    ARITHMETIC OPERATORS
+ - for addition(when used in numbers) and concatenation(when used in strings)
- - for subtraction
* - for multiplication
/ - for division

*/
/*
    COMPARISON OPERATORS
//- they return a boolean(true or false)
> - greater than
< - less than
>= - greater than or equal to
<= - less than or equal to

*/
/*
    EQUALITY OPERATORS
== - loose equals
eg 1 == "1"
=== - strict equals
eg 1 === 1
!== - strict does not equal

!= - loose does not equal

*/